// Type that captures the structure without literal types
type DeepStringify<T> = {
  [K in keyof T]: T[K] extends string
    ? string
    : T[K] extends readonly { title: string; description: string }[]
    ? { title: string; description: string }[]
    : DeepStringify<T[K]>
}

